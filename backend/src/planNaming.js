const _DAY_OPT = { 2:['周一','周四'], 3:['周一','周三','周五'], 4:['周一','周二','周四','周六'], 5:['周一','周二','周四','周五','周六'] };

export function generatePlanNames({
  goals, direction, weeklyFreq, membershipLevel, totalWeeks,
  level, goalKey, dirKey, freq,
} = {}) {
  const primaryGoal = (Array.isArray(goals) ? goals[0] : goals) || goalKey || 'muscle_gain';
  const resolvedGoal = GOAL_KEY_MAP[primaryGoal] || primaryGoal;
  const goalMatrix   = BLOCK_MATRIX[resolvedGoal] || BLOCK_MATRIX.muscle_gain;

  const resolvedFreq  = Math.min(5, Math.max(2, Number(weeklyFreq || freq) || 3));
  const resolvedWeeks = Math.max(1, Math.min(Number(totalWeeks) || 8, 52));
  const resolvedLevel = membershipLevel || level || 'standard';

  const availableKeys = [2, 4, 6, 8, 12];
  const closestKey    = availableKeys.reduce((p, c) => Math.abs(c - resolvedWeeks) < Math.abs(p - resolvedWeeks) ? c : p);
  const weekTemplates = goalMatrix[closestKey] || goalMatrix[4];

  const selectedDays = _DAY_OPT[resolvedFreq] || _DAY_OPT[3];

  const weeks = [];
  for (let i = 0; i < resolvedWeeks; i++) {
    const tpl     = weekTemplates[i < weekTemplates.length ? i : weekTemplates.length - 1];
    const phase   = tpl.phase;
    const isDeload = phase === 'deload';
    const weekFreq = isDeload ? Math.max(2, resolvedFreq - 1) : resolvedFreq;
    const days_list = (_DAY_OPT[weekFreq] || selectedDays).slice(0, weekFreq);
    const dayTpls  = tpl.dayTemplates[resolvedFreq] || tpl.dayTemplates[3] || tpl.dayTemplates[2];

    const days = days_list.map((day, di) => {
      const tmpl = dayTpls[di % dayTpls.length];
      return { day, name: tmpl, focus: tmpl };
    });

    weeks.push({
      week_num: i + 1,
      week_title: `Week ${i + 1} · ${tpl.theme}`,
      week_theme: tpl.theme,
      intensity_phase: phase,
      week_brief: tpl.brief,
      days,
    });
  }

  const goalLabel = { muscle_gain:'增肌', fat_loss:'减脂', performance:'运动表现', posture:'体态矫正', cardio:'心肺耐力', rehabilitation:'功能康复' };
  return {
    block_name: `${goalLabel[resolvedGoal] || resolvedGoal} · Block`,
    block_goal: resolvedGoal,
    membership_level: resolvedLevel,
    weeks,
  };
}
